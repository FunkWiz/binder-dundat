export function generateSpotifyAlbumUrl(id: string) {
  return `https://open.spotify.com/album/${id}`;
}

export function generateSpotifyTrackUrl(id: string) {
  return `https://open.spotify.com/track/${id}`;
}

export function generateYoutubeVideoUrl(id: string) {
  return `https://www.youtube.com/watch?v=${id}`;
}
